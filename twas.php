							Finally, we have our new member tier, <b>Beginners</b>.  Every new member to the clan starts as a Beginner.  Beginners can see their status either in-game or on their Bungie.Net clan page.  Beginners are "trial members" in a sense, who aren't yet full members of the clan.  Beginners cannot host Guided Games in Raids or Nightfall strikes, nor can they schedule events for the clan.  Normally, Beginners have 15 days from their join date to become full Members or they are subject to removal from the clan — <i>however</i>, given the large influx of Beginners in the clan with the membership drive, there is no  time restriction for the remainder of the current season.<br /><br />
							
							<span class="fa fa-user-plus"></span> To transition from Beginner to Member, a player simply needs to be a member of the clan Discord and play one activity with an established Member of the clan.  That can be anything from a raid to a strike to a Crucible match to a story mission to a few patrols.  Anything that takes 15 minutes or longer and utilises voice chat.  Once that's done, simply send a direct message on Discord to an Administrator on your platform, tell them what you did, who you were with, and that you'd like to be promoted.<br /><br />
							
